<h2>What is HIV?</h2>
<p>HIV (Human Immunodeficiency Virus) is a virus that attacks the body's immune system, specifically the CD4 cells (T cells), which help the immune system fight off infections. Untreated, HIV reduces the number of CD4 cells in the body, making the person more susceptible to infections and diseases.</p>
<p>HIV is transmitted through contact with certain body fluids of a person infected with HIV, such as blood, semen, vaginal fluids, and breast milk. The most common modes of transmission include:</p>
<ul>
    <li>Unprotected sexual intercourse with an HIV-infected person</li>
    <li>Sharing needles or syringes contaminated with HIV-infected blood</li>
    <li>Transmission from mother to child during pregnancy, childbirth, or breastfeeding</li>
</ul>
<p>HIV is not transmitted by casual contact such as hugging, kissing, shaking hands, sharing utensils, or using public restrooms.</p>
